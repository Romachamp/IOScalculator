import Foundation

class CalculatorFunctions {
    func plusNumbers(number1: Float, number2: Float) -> Float {
        return number1 + number2
    }
    
    func minusNumbers(number1: Float, number2: Float) -> Float {
        return number1 - number2
    }
    
    func divideNumbers(number1: Float, number2: Float) -> Float {
        return number1 / number2
    }
    
    func multiplyNumbers(number1: Float, number2: Float) -> Float {
        return number1 * number2
    }
}
